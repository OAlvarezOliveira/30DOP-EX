print(type(10))          # Int
print(type(3.14))        # Float
print(type(1 + 3j))      # Complex number
print(type('Asabeneh'))  # String
print(type([1, 2, 3]))   # List
print(type({'first_name':'Asabeneh','last_name':'Yetayeh','country':'Finland', 'age':250, 'is_married':True,'skills':['JS', 'React', 'Node', 'Python']})) # Dictionary
print(type({3.14, 9.81, 2.7}))    # Set
print(type(('Earth', 'Jupiter', 'Neptune', 'Mars', 'Venus', 'Saturn', 'Uranus', 'Mercury')))    # Tuple


